"""Adversarial (Tier-3) security summary artifact.

Renders the DEFENDED/BREACHED outcome of every adversarial probe into a
persistent markdown + JSON artifact, distinct from docs/defects-caught.md
(which documents the seeded-defect *design* catalog). This artifact reflects
an actual test run's measured security posture: how many probes per attack
category were defended vs. breached.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass
from pathlib import Path

from verity.adversarial import AdversarialProbe

_DEFAULT_MD_PATH = Path("reports/security/summary.md")
_DEFAULT_JSON_PATH = Path("reports/security/summary.json")

# Untracked scratch paths written by the pytest session-finish hook. Under
# `pytest -n auto` each xdist worker only sees its own subset of probes, so
# writing the tracked summary directly from the hook risks the last
# worker/controller to finish overwriting it with incomplete data. The
# tracked files above are only regenerated by `make redteam`, which runs the
# suite serially and promotes these local files afterward.
LOCAL_MD_PATH = Path("reports/security/summary-local.md")
LOCAL_JSON_PATH = Path("reports/security/summary-local.json")


@dataclass(frozen=True)
class CategoryTally:
    category: str
    defended: int
    breached: int

    @property
    def total(self) -> int:
        return self.defended + self.breached


@dataclass(frozen=True)
class SecuritySummary:
    total: int
    defended: int
    breached: int
    by_category: list[CategoryTally]
    probe_outcomes: dict[str, str]  # probe_id -> "DEFENDED" | "BREACHED"


def build_security_summary(
    vulnerability_results: dict[str, tuple[str, str]],
    probes: list[AdversarialProbe],
) -> SecuritySummary:
    """Build a SecuritySummary from per-probe (outcome, detail) results."""
    probes_by_id = {p.id: p for p in probes}
    by_category: dict[str, list[int]] = {}  # category -> [defended, breached]

    for probe_id, (outcome, _detail) in vulnerability_results.items():
        probe = probes_by_id.get(probe_id)
        category = probe.category if probe else "unknown"
        tally = by_category.setdefault(category, [0, 0])
        if outcome == "DEFENDED":
            tally[0] += 1
        else:
            tally[1] += 1

    category_tallies = [
        CategoryTally(category=cat, defended=d, breached=b)
        for cat, (d, b) in sorted(by_category.items())
    ]
    defended = sum(1 for o, _ in vulnerability_results.values() if o == "DEFENDED")
    breached = sum(1 for o, _ in vulnerability_results.values() if o == "BREACHED")

    return SecuritySummary(
        total=len(vulnerability_results),
        defended=defended,
        breached=breached,
        by_category=category_tallies,
        probe_outcomes={pid: outcome for pid, (outcome, _) in vulnerability_results.items()},
    )


def _authorization_boundary_lines(summary: SecuritySummary) -> list[str]:
    """Render the authorization-boundary section: what member-token gating does
    and does not prove, plus the measured cross-member probe outcomes from this
    run (see SECURITY.md for the full threat-model discussion)."""
    lines = [
        "## Authorization Boundary",
        "",
        "`CoverageAgent.answer()` accepts a `member_id` and an optional `member_token`. "
        "When `VERITY_MEMBER_AUTH_REQUIRED=true`, the request is rejected before any "
        "member data is loaded or any LLM call is made unless the token matches the "
        "static per-member mapping in `VERITY_MEMBER_TOKENS` "
        "(`src/sut/auth.py:member_token_valid`, tested by `tests/unit/test_member_auth.py`). "
        "**This is disabled by default** — `member_auth_required: bool = False` in "
        "`verity/config.py` — so a fresh clone runs with no request-level identity check.",
        "",
        "There is no session management, token issuance/rotation, or RBAC — every valid "
        "token grants full access to that one member's data, and the mapping is a static "
        "JSON blob, not a credential store.",
        "",
    ]

    cross_member = {
        pid: outcome
        for pid, outcome in summary.probe_outcomes.items()
        if pid.startswith("adv-crossmember")
    }
    if cross_member:
        lines += [
            "The cross-member probes below test whether retrieved context and LLM output "
            "stay scoped to the active member **when auth is off (the default)** — they do "
            "not by themselves prove identity enforcement; enable "
            "`VERITY_MEMBER_AUTH_REQUIRED` to exercise that boundary directly.",
            "",
            "| Probe ID | Outcome |",
            "|----------|---------|",
        ]
        for probe_id, outcome in sorted(cross_member.items()):
            marker = "✓ DEFENDED" if outcome == "DEFENDED" else "✗ BREACHED"
            lines.append(f"| `{probe_id}` | {marker} |")
        lines.append("")
    else:
        lines += [
            "No cross-member probes ran in this session — see "
            "`datasets/adversarial/probes.yaml` for the `adv-crossmember-*` probes.",
            "",
        ]
    return lines


def render_security_summary_markdown(summary: SecuritySummary) -> str:
    """Render a SecuritySummary as a markdown report."""
    lines = [
        "# Adversarial Security Summary",
        "",
        f"**Total probes:** {summary.total}  ",
        f"**Defended:** {summary.defended}  ",
        f"**Breached:** {summary.breached}",
        "",
        "## By Attack Category",
        "",
        "| Category | Defended | Breached | Total |",
        "|----------|---------:|---------:|------:|",
    ]
    for tally in summary.by_category:
        lines.append(f"| {tally.category} | {tally.defended} | {tally.breached} | {tally.total} |")
    lines += ["", "## Per-Probe Outcomes", "", "| Probe ID | Outcome |", "|----------|---------|"]
    for probe_id, outcome in sorted(summary.probe_outcomes.items()):
        marker = "✓ DEFENDED" if outcome == "DEFENDED" else "✗ BREACHED"
        lines.append(f"| `{probe_id}` | {marker} |")
    lines.append("")
    lines += _authorization_boundary_lines(summary)
    return "\n".join(lines)


def write_security_summary(
    summary: SecuritySummary,
    md_path: Path | None = None,
    json_path: Path | None = None,
) -> tuple[Path, Path]:
    """Write both markdown and JSON forms of a SecuritySummary to disk."""
    md_out = md_path or _DEFAULT_MD_PATH
    json_out = json_path or _DEFAULT_JSON_PATH
    md_out.parent.mkdir(parents=True, exist_ok=True)
    json_out.parent.mkdir(parents=True, exist_ok=True)
    md_out.write_text(render_security_summary_markdown(summary), encoding="utf-8")
    json_out.write_text(json.dumps(asdict(summary), indent=2), encoding="utf-8")
    return md_out, json_out
